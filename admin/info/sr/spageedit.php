<p>Ova strana vam omogućava da napravite strane koje služe da se korisnici prijave na vaše liste.</p>
<p>Možete izabrati bilo koji skup atributa i listi koji su definisani u vašem sistemu. Ako lista ili
atribut nije izlistan ovde, prvo ih morate dodati. Podrazumevane vrednosti atributa će biti kao globalne
vrednosti atributa, ali ih možete presložiti, i dodati drugačije podrazumevane vrednosti. Takođe možete
da promenite da li je atribut obavezan ili ne. Ovo će imati efekta samo na obaveznost atributa kada
korisnici koriste stranu za prijavu.</p>